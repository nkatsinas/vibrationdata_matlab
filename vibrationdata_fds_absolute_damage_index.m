% --- Executes on button press in pushbutton_calculate.
function pushbutton_calculate_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton_calculate (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

try

    Figures = findobj( 'Type', 'Figure' , '-not' , 'Tag' , get( handles.output , 'Tag' ) );
    NFigures = length( Figures );
    for nFigures = 1 : NFigures
        close( Figures( nFigures ) );
    end
catch
end  

disp(' ');
disp(' * * * * * * * * * * * ');
disp(' ');

fig_num=1;

try
    FS=strtrim(get(handles.edit_input_array,'String'));
    THM=evalin('base',FS);   
catch
    warndlg('Input Filename Error');
    return;
    
end

if(THM(:,1)<1.0e-20)
    THM(1,:)=[];
end

fmin=THM(1,1);
fmax=THM(end,1);

sdur=get(handles.edit_dur,'String');

if isempty(sdur)
    set(handles.edit_dur,'String','60');    
end    

T=str2num(get(handles.edit_dur,'String'));

velox_limit=str2num(get(handles.edit_velox,'String'));

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

Q=str2double(get(handles.edit_Q,'String'));
b=str2double(get(handles.edit_bex,'String'));

nb=length(b);
nq=length(Q);

A=zeros(nb,1);

for i=1:nb
    A(i)=(2*velox_limit)^b(i);
end

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

f=THM(:,1);
a=THM(:,2);

[s,rms]=calculate_PSD_slopes(f,a);

df=0.5;
%
[fi,ai]=interpolate_PSD(f,a,s,df);

fi=fix_size(fi);
ai=fix_size(ai);

base_input_psd_int=[fi,ai];
%

[fn,~]=octaves(5);  % 1/24

[~,i1]=min(abs(fn-fmin));
[~,i2]=min(abs(fn-fmax));

fn=fn(i1:i2);
nf=length(fn);

fn=fix_size(fn);
omegan=2*pi*fn;

FDS=zeros(nf,4);

k=0;

for iq=1:nq  % Q
    for j=1:nb  % b

        k=k+1;
        for i=1:nf
            [accel_response_psd]=sdof_psd_response_base_alt(fn(i),Q(iq),base_input_psd_int);
            pv_response_psd=[accel_response_psd(:,1)  386.^2*accel_response_psd(:,2)/omegan(i)^2];
    
            [damage,~,~]=Dirlik_response_psd_alt(pv_response_psd,A(j),b(j),T);
            FDS(i,k)=damage;
            
        end
    end
end

disp(' Maximum Absolute Damage  (failure for damage >=1 ) ');
disp(' ');

out1=sprintf(' Q=10 b=6.4:  %8.4g',max(FDS(:,1)));
out2=sprintf(' Q=30 b=6.4:  %8.4g',max(FDS(:,2)));

out3=sprintf(' Q=30 b=4:  %8.4g',max(FDS(:,3)));
out4=sprintf(' Q=30 b=8:  %8.4g',max(FDS(:,4)));

disp(out1);
disp(out2);


% A(1)
% A(2)

ylab='Accel (G^2/Hz)';
xlab='Frequency (Hz)';
t_string=sprintf('Base Input PSD  %7.3g GRMS overall',rms);
[fig_num,~]=plot_PSD_function(fig_num,xlab,ylab,t_string,THM,fmin,fmax);
    
    
